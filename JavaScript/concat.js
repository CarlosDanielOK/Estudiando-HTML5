// concat() Combina dos o más arrays, devolviendo un nuevo array sin modificar los originales. Uso común: Unir múltiples arrays en uno solo.

const array1 = [1, 2];
const array2 = [3, 4];
const combined = array1.concat(array2); // concatena el array3 al array1
console.log(combined); // [1, 2, 3, 4]
